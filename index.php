      <script>
        document.addEventListener('DOMContentLoaded', function() {
          const partnerWrappers = document.querySelectorAll('.partner-logo-wrapper');
          let tooltip = null;

          // Create tooltip element
          tooltip = document.createElement('div');
          tooltip.className = 'partner-tooltip';
          document.body.appendChild(tooltip);

          partnerWrappers.forEach(wrapper => {
            wrapper.addEventListener('mouseenter', function(e) {
              const name = this.getAttribute('data-partner-name');
              const description = this.getAttribute('data-partner-description');

              tooltip.innerHTML = `
                <h5>${name}</h5>
                <p>${description}</p>
              `;

              tooltip.classList.add('active');
            });

            wrapper.addEventListener('mousemove', function(e) {
              const tooltipWidth = tooltip.offsetWidth;
              const tooltipHeight = tooltip.offsetHeight;

              let left = e.clientX + 20;
              let top = e.clientY + 20;

              // Keep tooltip within viewport
              if (left + tooltipWidth > window.innerWidth) {
                left = e.clientX - tooltipWidth - 20;
              }

              if (top + tooltipHeight > window.innerHeight) {
                top = e.clientY - tooltipHeight - 20;
              }

              tooltip.style.left = left + 'px';
              tooltip.style.top = top + 'px';
            });

            wrapper.addEventListener('mouseleave', function() {
              tooltip.classList.remove('active');
            });
          });
        });
      </script>

